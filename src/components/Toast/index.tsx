import { ToastContainer, toast } from "react-toastify";

import "react-toastify/dist/ReactToastify.css";

export const toastSuccess = (messages: string) => {
    toast.success(`${messages}`, {
        position: "bottom-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "colored",
    });
};

